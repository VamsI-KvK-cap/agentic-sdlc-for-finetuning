def reader_node(state: AgentState):
    """
    Read contents of files that need to be updated.

    Parameters:
        state (AgentState):
            Must contain:
                - plan (dict): output from planner

    Returns:
        AgentState:
            Updated state with:
                - existing_files (dict[path → content])

    Behavior:
        - Extracts files marked with action="update"
        - Reads file contents using read_file tool
        - Stores results in state["existing_files"]

    Notes:
        - Only reads files that will be modified
        - Skips "create" files (since they don't exist yet)
    """

    logger.info("Executing Reader Node")

    plan = state["plan"]

    # Extract only files that need updates
    files_to_update = [
        file["path"] for file in plan["files"]  # ignore type-check
        if file["action"] == "update"
    ]

    existing_files = {}

    # Read each file's content
    for path in files_to_update:
        content = read_file.invoke(path)
        existing_files[path] = content  # store path → content mapping

    # Store in state
    state["existing_files"] = existing_files

    logger.debug(f"Existing Files: \n{existing_files}")

    return state


def should_read(state: AgentState) -> Literal["reader", "skip_reader"]:
    """
    Decide whether the reader node should be executed.

    Parameters:
        state (AgentState):
            Must contain plan

    Returns:
        Literal["reader", "skip_reader"]:
            - "reader": if any file requires update
            - "skip_reader": if only new files are created

    Behavior:
        - Iterates through planned files
        - Checks if any file has action="update"
        - Handles exceptions safely

    Notes:
        - Prevents unnecessary file reads
        - Improves performance when only creating new files
    """

    plan = state["plan"]

    try:
        # Check if any file needs updating
        for file in plan["files"]:
            if file["action"] == "update":
                return "reader"

        # No updates → skip reader
        return "skip_reader"

    except FileNotFoundError:
        # If plan structure is invalid or missing
        return "skip_reader"

    except Exception as e:
        print(f"Error occured: {e}")
        return "skip_reader"


