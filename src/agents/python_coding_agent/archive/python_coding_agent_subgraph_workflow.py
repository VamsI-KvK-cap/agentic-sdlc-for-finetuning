def fan_out_node(state: AgentState) -> List[Send]:
    """Dispatch one subgraph execution per file in the plan."""

    """
    Create parallel execution tasks (fan-out) for each file in the plan.

    Parameters:
        state (AgentState):
            Must contain:
                - plan["files"]: list of FilePlan objects
                - execution metadata (execution_id, work_dir, etc.)

    Returns:
        List[Send]:
            List of Send objects, each triggering a subgraph execution.

    Behavior:
        - Iterates through all files in the plan
        - Creates a separate FileAgentState for each file
        - Passes file-specific context into subgraph
        - Enables parallel or independent execution

    Notes:
        - This is a classic "fan-out" pattern in LangGraph
        - Each file is processed independently in the subgraph
        - Helps scale multi-file operations efficiently
    """

    sends = []

    # Iterate through each file in the execution plan
    for file_plan in state["plan"]["files"]:

        # Default: no existing content
        existing_content = None

        # If file is being updated, fetch existing content
        if file_plan["action"] == "update":
            existing_content = state.get("existing_files", {}).get(file_plan["path"])

        # Create a Send object to trigger subgraph execution
        sends.append(Send(
            "file_subgraph",  # target node (subgraph)
            FileAgentState(
                execution_id=state["execution_id"],  # execution context
                work_dir=state["work_dir"],  # working directory
                task=state["task"],  # user task
                feedback=state.get("feedback"),  # feedback from previous loop
                retry_count=state.get("retry_count"),  # retry tracking
                written_files=state.get("written_files"),  # accumulated outputs

                # File-specific context
                file_plan=file_plan,
                existing_file_content=existing_content,

                # Initialize per-file state
                code_change=None,
                review=None,
            )
        ))

    return sends


def fan_out_edges(state: AgentState) -> List[Send]:
    """
    Alternative fan-out implementation using dictionary-based state.

    Parameters:
        state (AgentState)

    Returns:
        List[Send]:
            Send objects for subgraph execution

    Behavior:
        - Uses list comprehension instead of loop
        - Constructs state as dictionary instead of TypedDict instance
        - Functionally equivalent to fan_out_node

    Notes:
        - Often used in conditional edges in LangGraph
        - More concise but slightly less explicit than fan_out_node
    """

    return [
        Send(
            "file_subgraph",
            {
                "execution_id": state["execution_id"],
                "work_dir": state["work_dir"],
                "task": state["task"],
                "feedback": state.get("feedback"),
                "retry_count": state.get("retry_count"),
                "written_files": state.get("written_files"),

                # Current file plan
                "file_plan": file_plan,

                # Only include content if updating
                "existing_file_content": (
                    (state.get("existing_files") or {}).get(file_plan["path"])
                    if file_plan["action"] == "update"
                    else None
                ),

                # Initialize execution state
                "code_change": None,
                "review": None,
            }
        )
        for file_plan in state["plan"]["files"]
    ]


