def append_list(existing, new):
    """
    Reducer that accumulates list results from parallel subgraphs.

    This function is used in conjunction with LangGraph's `Annotated`
    fields to merge outputs from multiple parallel executions.

    Parameters:
        existing (list | None):
            Previously accumulated list of values.

        new (list | None):
            Newly returned list of values from a node/subgraph.

    Returns:
        list:
            Combined list containing both existing and new values.

    Behavior:
        - Handles None values safely
        - Ensures lists are concatenated instead of overwritten

    Notes:
        - Critical for fan-out/fan-in workflows
        - Used for aggregating results like `code_changes`, `written_files`
    """

    # If either is None, replace with empty list before concatenation
    return (existing or []) + (new or [])


class AgentState(TypedDict):
    """
    Global state object shared across all nodes in the workflow.

    This state is passed between nodes in the LangGraph execution pipeline
    and gets incrementally updated as the workflow progresses.

    Fields:
        execution_id (int):
            Unique identifier for the current execution run.

        work_dir (str):
            Absolute path to the working directory for this execution.

        task (str):
            User-provided task description.

        file_structure (str):
            Snapshot of the repository structure (tree format).

        existing_files (Optional[Dict[str, str]]):
            Mapping of file path → file content (used for updates).

        plan (Optional[Dict[str, Any]]):
            Structured plan generated by the planner node.

        code_changes (Annotated):
            Aggregated list of code changes across all files.
            Uses `append_list` reducer to merge results.

        written_files (Annotated):
            List of file paths written to disk.
            Uses `append_list` reducer.

        review (Optional[Dict[str, str]]):
            Reviewer output (status, issues, suggestions).

        static_check_success (Optional[bool]):
            Whether static analysis passed.

        static_check_output (Optional[str]):
            Raw output from static analysis tool.

        feedback (Optional[str]):
            Feedback passed back into retry loops.

        retry_count (Dict[str, int]):
            Tracks retry attempts for different stages:
                - review
                - static_check_count
    """

    execution_id: int
    work_dir: str
    task: str
    file_structure: str
    existing_files: Optional[Dict[str, str]]  # path -> content
    plan: Optional[Dict[str, Any]]

    # Annotated with reducer to merge parallel results
    code_changes: Annotated[Optional[Dict[str, Any]], append_list]
    written_files: Annotated[Optional[List[str]], append_list]

    review: Optional[Dict[str, str]]
    static_check_success: Optional[bool]
    static_check_output: Optional[str]
    feedback: Optional[str]
    retry_count: Dict[str, int]


