def review_descision(state: FileAgentState):
    """
    Decide next step based on reviewer output.

    Parameters:
        state (FileAgentState):
            Must contain:
                - review (dict)
                - retry_count (dict)

    Returns:
        str:
            One of:
                - "approve" → proceed to writer
                - "revise" → retry coder
                - "abort" → stop execution

    Behavior:
        - If approved → reset retry counter
        - If not approved:
            → increment retry count
            → check retry limit
            → either retry or abort

    Notes:
        - Controls feedback loop between coder and reviewer
        - Prevents infinite retry cycles
    """

    # Check reviewer decision
    if state["review"]["status"].lower() == "approved":
        state["retry_count"]["review"] = 0  # reset retry counter
        return "approve"

    # Increment retry count
    state["retry_count"]["review"] += 1

    # If retry limit exceeded → abort
    if state["retry_count"]["review"] >= MAX_REVIEW_RETRIES:
        return "abort"

    # Otherwise → retry coder
    return "revise"


